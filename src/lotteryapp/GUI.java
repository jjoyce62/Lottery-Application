    private void saveNumbersL1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveNumbersL1ActionPerformed
//        fav1.setEnabled(false);
//        fav1.show(false);

        File outFile;  //1.Create an instance of File class
        FileWriter fw;  //2.Create Charachter Stream
        BufferedWriter bw;//3.Create a Buffered Stream

        int[] saveNumbers1 = getUserLine1();

        try {
            outFile = new File("savedNumbers.txt");
            fw = new FileWriter(outFile, true);
            bw = new BufferedWriter(fw);

            for (int i = 0; i < saveNumbers1.length; i++) {
                if (i == 0) {
                    bw.write("" + saveNumbers1[i]); //Write array members to the file without "," at the start
                } else {
                    bw.write("," + saveNumbers1[i]);//Add "," in between numbers for the split method
                }

                //  System.out.println(saveNumbers1[i]);
            }
            bw.newLine();       //Bring to the new line
            bw.close();
            JOptionPane.showMessageDialog(null, "Your Numbers Have Been Saved, God Bless You");
        } catch (IOException e) {
            System.out.println("Error writing to file: " + e);
        }


    }//GEN-LAST:event_saveNumbersL1ActionPerformed

