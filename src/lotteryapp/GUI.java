    //  Return int array of values from the textfields of line 1
    private int[] getUserLine1() {
        //  Get the values from the input boxes
        String input1 = Line1b1.getText();
        String input2 = Line1b2.getText();
        String input3 = Line1b3.getText();
        String input4 = Line1b4.getText();
        String input5 = Line1b5.getText();

        //  Make sure none are empty
        if (input1.isEmpty() || input2.isEmpty() || input3.isEmpty() || input4.isEmpty() || input5.isEmpty()) {
            //  If there is an empty box, return an empty array
            //  This is because an empty string will crash Integer.parseInt
            //  and the array size is 0 so it doesn't loop through when checking
            //  for matching numbers.
            return new int[0];
        }

        int num1 = Integer.parseInt(input1);
        int num2 = Integer.parseInt(input2);
        int num3 = Integer.parseInt(input3);
        int num4 = Integer.parseInt(input4);
        int num5 = Integer.parseInt(input5);
        int[] line1 = {num1, num2, num3, num4, num5};
        return line1;
    }

